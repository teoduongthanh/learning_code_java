import java.util.Scanner;

public class ChuyenDoiDuLieu {
	public static void main(String[] args) {
		int a = 79;
		int b = 30;
		
		System.out.println("values A = "+ a);
		System.out.println("values B = "+ b);
		// chuyn doi du lieu kieu ngam dinh
		float c = a;
		float d = b;
		System.out.println("values c = "+ c);
		System.out.println("values d = "+ d);
		
		// chuyen doi kieu du lieu kieu tuong minnh;
		float e = 3.5f;
		float f = 9.5f;
		System.out.println("values e = "+ e);
		System.out.println("values f = "+ f);
		 
		int g = (int)e;
		int j = (int)f;
		System.out.println("values g = "+ g);
		System.out.println("values j = "+ j);
				
	}
}

